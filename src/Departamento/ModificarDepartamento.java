    private void btn_ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ModificarActionPerformed
        // TODO add your handling code here:
        
        int idDepartamento = Integer.parseInt(txt_Id.getText().trim());
        String nuevoNombre = txt_NombreDepartamento.getText().trim();

        ConexionSQL conexion = new ConexionSQL(); 
        Connection conn = conexion.conectar();

        try {
            CallableStatement cs = conn.prepareCall("{call SP_Modificar_Departamento(?, ?, ?)}");
            cs.setInt(1, idDepartamento);
            cs.setString(2, nuevoNombre);
            cs.registerOutParameter(3, java.sql.Types.VARCHAR);

            cs.execute();

            String mensaje = cs.getString(3);
            JOptionPane.showMessageDialog(null, mensaje);

            if (mensaje.equalsIgnoreCase("El departamento fue modificado correctamente.")) {
                txt_Id.setText("");
                txt_NombreDepartamento.setText("");
            }

            cs.close();
            conexion.desconectar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error SQL: " + ex.getMessage());
        }

        
    }//GEN-LAST:event_btn_ModificarActionPerformed


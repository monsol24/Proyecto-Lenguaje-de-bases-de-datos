    public void cargarCursosAsignados() {
        ConexionSQL conexion = new ConexionSQL();
        Connection conn = conexion.conectar();
        CallableStatement cs = null;
        ResultSet rs = null;

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Asignaci√≥n");
        modelo.addColumn("ID Curso");
        modelo.addColumn("Periodo");

        try {
            cs = conn.prepareCall("{ call SP_Listar_Cursos_Asignados_Profesores(?) }");
            cs.registerOutParameter(1, oracle.jdbc.OracleTypes.CURSOR);
            cs.execute();

            rs = (ResultSet) cs.getObject(1);

            boolean hayDatos = false;
            while (rs.next()) {
                hayDatos = true;
                Object[] fila = new Object[3];
                fila[0] = rs.getInt("id_asignacion");
                fila[1] = rs.getInt("id_curso");
                fila[2] = rs.getString("periodo");
                modelo.addRow(fila);
            }

            if (!hayDatos) {
                JOptionPane.showMessageDialog(null, "No hay cursos asignados al profesor actual.");
            }

            tbl_CursosAsignados.setModel(modelo);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar cursos asignados: " + e.getMessage());
        } finally {
            try {
                if (rs != null) rs.close();
                if (cs != null) cs.close();
                if (conn != null) conn.close();
            } catch (SQLException ex) {
            }
        }
    }

    

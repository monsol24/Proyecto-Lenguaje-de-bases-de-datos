    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed
        // TODO add your handling code here:
        
        int idNota = Integer.parseInt(Txt_Id.getText().trim());
        ConexionSQL conexion = new ConexionSQL();
        Connection conn = conexion.conectar();

        try {
            CallableStatement cs = conn.prepareCall("{call SP_Eliminar_Nota_Profesor(?, ?)}");

            cs.setInt(1, idNota);
            cs.registerOutParameter(2, java.sql.Types.VARCHAR);

            cs.execute();

            String mensaje = cs.getString(2);
            JOptionPane.showMessageDialog(null, mensaje);

            if (mensaje.equalsIgnoreCase("La nota fue eliminada correctamente.")) {
                Txt_Id.setText("");
            }

            cs.close();
            conexion.desconectar();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error: ID inv√°lido.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error SQL: " + ex.getMessage());
        }


    }//GEN-LAST:event_btn_EliminarActionPerformed


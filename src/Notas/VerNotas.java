    private void cargarDatos(Integer idNota) {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "ID Nota",
            "Estudiante",
            "Curso",
            "Nota 1",
            "Nota 2",
            "Nota 3",
            "Promedio",
            "Trimestre"
        });
        ConexionSQL cn = new ConexionSQL();
        Connection conn = cn.conectar();

        try {
            CallableStatement cs;

            if (idNota == null) {
                cs = conn.prepareCall("{ call PKG_NOTA.SP_Listar_Notas(?) }");
                cs.registerOutParameter(1, OracleTypes.CURSOR);
                cs.execute();

                ResultSet rs = (ResultSet) cs.getObject(1);
                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getInt("id_nota"),
                        rs.getString("estudiante"), // concat nombre + apellido
                        rs.getString("nombre_curso"),
                        rs.getDouble("nota1"),
                        rs.getDouble("nota2"),
                        rs.getDouble("nota3"),
                        rs.getDouble("promedio"),
                        rs.getString("trimestre")
                    });
                }
                rs.close();
            } else {
                cs = conn.prepareCall("{ call PKG_NOTA.SP_Buscar_Nota(?, ?) }");
                cs.setInt(1, idNota);
                cs.registerOutParameter(2, OracleTypes.CURSOR);
                cs.execute();

                ResultSet rs = (ResultSet) cs.getObject(2);
                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getInt("id_nota"),
                        rs.getString("estudiante"),
                        rs.getString("nombre_curso"),
                        rs.getDouble("nota1"),
                        rs.getDouble("nota2"),
                        rs.getDouble("nota3"),
                        rs.getDouble("promedio"),
                        rs.getString("trimestre")
                    });
                }
                rs.close();
            }

            cs.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al listar notas: " + ex.getMessage());
        } finally {
            cn.desconectar();
        }

        tblNotas.setModel(model);
    }


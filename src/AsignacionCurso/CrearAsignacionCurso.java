    private void btn_registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registroActionPerformed

        ConexionSQL conexion = new ConexionSQL();
        Connection conn = conexion.conectar();
        CallableStatement cs = null;

        try {
            int idProfesor = Integer.parseInt(txt_IdProfesor.getText().trim());
            int idCurso = Integer.parseInt(txt_IdCurso.getText().trim());
            String periodo = Cmb_Periodo.getSelectedItem().toString();

            cs = conn.prepareCall("{ call SP_Agregar_AsignacionCurso(?, ?, ?, ?) }");
            cs.setInt(1, idProfesor);
            cs.setInt(2, idCurso);
            cs.setString(3, periodo);
            cs.registerOutParameter(4, java.sql.Types.VARCHAR);

            cs.execute();
            String mensaje = cs.getString(4);
            JOptionPane.showMessageDialog(null, mensaje);

            if (mensaje.equals("Asignación de curso agregada correctamente.")) {
                txt_IdProfesor.setText("");
                txt_IdCurso.setText("");
                Cmb_Periodo.setSelectedIndex(0);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID inválido. Ingrese números.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error en base de datos: " + e.getMessage());
        } finally {
            try {
                if (cs != null) cs.close();
                if (conn != null) conn.close();
            } catch (SQLException ex) {
            }
}


    }//GEN-LAST:event_btn_registroActionPerformed


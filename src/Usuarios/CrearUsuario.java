    private void btn_registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registroActionPerformed
        // TODO add your handling code here:
        String nombreUsuario = txt_usuario.getText().trim();
        String contrasena = txt_contrasena.getText().trim();
        String rol = cmb_rol.getSelectedItem().toString(); 

        ConexionSQL conexion = new ConexionSQL();
        Connection conn = conexion.conectar();

        try {
            CallableStatement cs = conn.prepareCall("{call SP_Insertar_Usuario(?, ?, ?, ?)}");
            cs.setString(1, nombreUsuario);
            cs.setString(2, contrasena);
            cs.setString(3, rol);
            cs.registerOutParameter(4, java.sql.Types.VARCHAR);
            cs.execute();

            String mensaje = cs.getString(4);
            JOptionPane.showMessageDialog(null, mensaje);

            if (mensaje.contains("correctamente")) {
                txt_contrasena.setText("");
                txt_usuario.setText("");
                cmb_rol.setSelectedIndex(0);
            }

            cs.close();
            conexion.desconectar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error SQL: " + ex.getMessage());
        }

    }//GEN-LAST:event_btn_registroActionPerformed


    private void btn_ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ModificarActionPerformed
        // TODO add your handling code here:
        int idUsuario = Integer.parseInt(txt_id.getText().trim());
        String nombreUsuario = txt_usuario.getText().trim();
        String contrasena = txt_contrasena.getText().trim();
        String rol = cmb_rol.getSelectedItem().toString();

        ConexionSQL conexion = new ConexionSQL();
        Connection conn = conexion.conectar();

        try {
            CallableStatement cs = conn.prepareCall("{call SP_Modificar_Usuario(?, ?, ?, ?, ?)}");
            cs.setInt(1, idUsuario);
            cs.setString(2, nombreUsuario);
            cs.setString(3, contrasena);
            cs.setString(4, rol);
            cs.registerOutParameter(5, java.sql.Types.VARCHAR);

            cs.execute();

            String mensaje = cs.getString(5);
            JOptionPane.showMessageDialog(null, mensaje);

            if (mensaje.contains("correctamente")) {
                txt_id.setText("");
                txt_usuario.setText("");
                txt_contrasena.setText("");
                cmb_rol.setSelectedIndex(0);
            }

            cs.close();
            conexion.desconectar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error SQL: " + ex.getMessage());
        }

    }//GEN-LAST:event_btn_ModificarActionPerformed


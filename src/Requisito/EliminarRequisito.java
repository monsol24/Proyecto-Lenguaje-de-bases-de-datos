    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed
        // TODO add your handling code here:
        
        ConexionSQL conexion = new ConexionSQL();
        Connection conn = conexion.conectar();
        CallableStatement cs = null;

        try {
            int id = Integer.parseInt(txt_Id.getText().trim());

            cs = conn.prepareCall("{call ELIMINAR_REQUISITO(?, ?)}");
            cs.setInt(1, id);
            cs.registerOutParameter(2, Types.VARCHAR); 

            cs.execute();

            String mensaje = cs.getString(2);
            JOptionPane.showMessageDialog(null, mensaje);

            if (mensaje.equals("Requisito eliminado correctamente.")) {
                txt_Id.setText("");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al eliminar: " + ex.getMessage());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "ID inválido. Ingrese un número.");
        } finally {
            try {
                if (cs != null) cs.close();
                if (conn != null) conn.close();
            } catch (SQLException e) {
                
            }
        }

        
    }//GEN-LAST:event_btn_EliminarActionPerformed


    private void cargarDatos(Integer idRequisito) {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"ID", "Descripción"});
        ConexionSQL cn = new ConexionSQL();
        Connection conn = cn.conectar();

        try {
            CallableStatement cs;
            if (idRequisito == null) {
                cs = conn.prepareCall("{ call PKG_REQUISITO.SP_Listar_Requisitos(?) }");
                cs.registerOutParameter(1, OracleTypes.CURSOR);
                cs.execute();
                ResultSet rs = (ResultSet) cs.getObject(1);
                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getInt("id_requisito"),
                        rs.getString("descripcion")
                    });
                }
                rs.close();
            } else {
                cs = conn.prepareCall("{ call PKG_REQUISITO.SP_Buscar_Requisito(?, ?) }");
                cs.setInt(1, idRequisito);
                cs.registerOutParameter(2, OracleTypes.CURSOR);
                cs.execute();
                ResultSet rs = (ResultSet) cs.getObject(2);
                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getInt("id_requisito"),
                        rs.getString("descripcion")
                    });
                }
                rs.close();
            }
            cs.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al listar requisitos: " + ex.getMessage());
        } finally {
            cn.desconectar();
        }

        tblReq.setModel(model);
    }


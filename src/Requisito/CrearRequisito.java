    private void btn_Agregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Agregar1ActionPerformed
        // TODO add your handling code here:
        String descripcion = txt_Descripcion.getText().trim();

        ConexionSQL conexion = new ConexionSQL();
        Connection conn = conexion.conectar();
        CallableStatement cs = null;

        try {
            cs = conn.prepareCall("{call SP_Agregar_Requisito(?, ?)}");
            cs.setString(1, descripcion);
            cs.registerOutParameter(2, java.sql.Types.VARCHAR);

            cs.execute();

            String mensaje = cs.getString(2);
            JOptionPane.showMessageDialog(null, mensaje);

            if (mensaje.equalsIgnoreCase("El requisito se agregó correctamente.")) {
                txt_Descripcion.setText("");
            }

            cs.close();
            conexion.desconectar();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error SQL: " + e.getMessage());
        }

        
    }//GEN-LAST:event_btn_Agregar1ActionPerformed


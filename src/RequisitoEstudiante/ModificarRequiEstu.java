    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed

        ConexionSQL conexion = new ConexionSQL();
        Connection conn = conexion.conectar();
        CallableStatement cs = null;

        try {
            cs = conn.prepareCall("{ call SP_Modificar_RequisitoEstudiante(?, ?, ?, ?, ?) }");
            cs.setInt(1, Integer.parseInt(txt_RequisitoEstudiante.getText()));
            cs.setInt(2, Integer.parseInt(txt_IdEstudiante.getText()));
            cs.setInt(3, Integer.parseInt(txt_IdRequisito.getText()));
            cs.setString(4, Cmb_Cumplido.getSelectedItem().toString());
            cs.registerOutParameter(5, java.sql.Types.VARCHAR);

            cs.execute();
            String mensaje = cs.getString(5);
            JOptionPane.showMessageDialog(null, mensaje);

            if (mensaje.equals("Requisito estudiante modificado correctamente.")) {
                txt_RequisitoEstudiante.setText("");
                txt_IdEstudiante.setText("");
                txt_IdRequisito.setText("");
                Cmb_Cumplido.setSelectedIndex(0);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        } finally {
            try { if (cs != null) cs.close(); if (conn != null) conn.close(); } catch (Exception ex) {}
        }

    }//GEN-LAST:event_btn_modificarActionPerformed


    private void cargarAccesos() {
        ConexionSQL conexion = new ConexionSQL();
        Connection conn = conexion.conectar();
        CallableStatement cs = null;
        ResultSet rs = null;

        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Acceso");
        modelo.addColumn("ID Usuario");
        modelo.addColumn("ID Departamento");
        modelo.addColumn("Fecha Ingreso");

        try {
            cs = conn.prepareCall("{ call SP_Listar_Accesos(?) }");
            cs.registerOutParameter(1, oracle.jdbc.OracleTypes.CURSOR);
            cs.execute();
            rs = (ResultSet) cs.getObject(1);

            while (rs.next()) {
                Object[] fila = new Object[4];
                fila[0] = rs.getInt("id_acceso");
                fila[1] = rs.getInt("id_usuario");
                fila[2] = rs.getInt("id_departamento");
                fila[3] = rs.getDate("fecha_ingreso");
                modelo.addRow(fila);
            }

            tbl_Accesos.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al mostrar accesos: " + e.getMessage());
        } finally {
            try {
                if (rs != null) rs.close();
                if (cs != null) cs.close();
                if (conn != null) conn.close();
            } catch (SQLException ex) {
                
            }
        }
    }
    

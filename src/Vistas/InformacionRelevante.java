    private void btn_VerNombre_Completo1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_VerNombre_Completo1ActionPerformed
        // TODO add your handling code here:
         try {
            int idProfesor = Integer.parseInt(txt_IdProfesor.getText().trim());

            ConexionSQL conexion = new ConexionSQL();
            Connection conn = conexion.conectar();

            CallableStatement cs = conn.prepareCall("{ ? = call FN_TOTAL_ASIGNACIONES(?) }");
            cs.registerOutParameter(1, java.sql.Types.VARCHAR);
            cs.setInt(2, idProfesor);

            cs.execute();

            String mensaje = cs.getString(1);
            JOptionPane.showMessageDialog(this, mensaje);

            cs.close();
            conn.close();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID inválido. Ingrese un número entero.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error SQL: " + ex.getMessage());
        }
    }//GEN-LAST:event_btn_VerNombre_Completo1ActionPerformed


    private void btn_VerNombre_CompletoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_VerNombre_CompletoActionPerformed
        // TODO add your handling code here:
        try {
            int idEstudiante = Integer.parseInt(txt_IdEstudiante.getText().trim());

            ConexionSQL conexion = new ConexionSQL();
            Connection conn = conexion.conectar();

            CallableStatement cs = conn.prepareCall("{ ? = call fn_nombre_completo_estudiante(?) }");
            cs.registerOutParameter(1, java.sql.Types.VARCHAR);
            cs.setInt(2, idEstudiante);

            cs.execute();

            String resultado = cs.getString(1);
            JOptionPane.showMessageDialog(this, resultado);

            cs.close();
            conn.close();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID inválido. Ingrese un número entero.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al consultar el nombre: " + ex.getMessage());
        }
    }//GEN-LAST:event_btn_VerNombre_CompletoActionPerformed


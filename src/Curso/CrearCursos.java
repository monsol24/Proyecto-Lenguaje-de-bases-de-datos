    private void btn_registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registroActionPerformed

        String nombre = txt_NombreCurso.getText().trim();
        String codigo = txt_CodigoCurso.getText().trim();
        String creditosStr = txt_CreditosCurso.getText().trim();
        String descripcion = txt_DescripcionCurso.getText().trim();
        int creditos = Integer.parseInt(creditosStr);

        ConexionSQL conexion = new ConexionSQL();
        Connection conn = conexion.conectar();

        try {

            CallableStatement cs = conn.prepareCall("{call SP_Agregar_Curso(?, ?, ?, ?, ?)}");

            cs.setString(1, nombre);
            cs.setString(2, codigo);
            cs.setInt(3, creditos);
            cs.setString(4, descripcion);
            cs.registerOutParameter(5, java.sql.Types.VARCHAR);

            cs.execute();

            String mensaje = cs.getString(5);
            JOptionPane.showMessageDialog(null, mensaje);

            if (mensaje.equalsIgnoreCase("El curso se agregó correctamente.")) {
                txt_NombreCurso.setText("");
                txt_CodigoCurso.setText("");
                txt_CreditosCurso.setText("");
                txt_DescripcionCurso.setText("");
            }

            cs.close();
            conexion.desconectar();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Los créditos deben ser numéricos.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error SQL: " + ex.getMessage());
        }


    }//GEN-LAST:event_btn_registroActionPerformed


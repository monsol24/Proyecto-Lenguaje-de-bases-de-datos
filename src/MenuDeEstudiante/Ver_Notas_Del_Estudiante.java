public void cargarNotasEstudiante() {
    ConexionSQL conexion = new ConexionSQL();
    Connection conn = conexion.conectar();
    CallableStatement cs = null;
    ResultSet rs = null;

    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID Nota");
    modelo.addColumn("ID Matr√≠cula");
    modelo.addColumn("Nota 1");
    modelo.addColumn("Nota 2");
    modelo.addColumn("Nota 3");
    modelo.addColumn("Promedio");
    modelo.addColumn("Trimestre");

    try {
        cs = conn.prepareCall("{ call SP_Listar_Notas_Del_Estudiante(?) }");
        cs.registerOutParameter(1, oracle.jdbc.OracleTypes.CURSOR);
        cs.execute();

        rs = (ResultSet) cs.getObject(1);

        while (rs.next()) {
            Object[] fila = new Object[7];
            fila[0] = rs.getInt("id_nota");
            fila[1] = rs.getInt("id_matricula");
            fila[2] = rs.getInt("nota1");
            fila[3] = rs.getInt("nota2");
            fila[4] = rs.getInt("nota3");
            fila[5] = rs.getInt("promedio");
            fila[6] = rs.getInt("trimestre");
            modelo.addRow(fila);
        }

        tbl_NotasEstudiante.setModel(modelo);

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al cargar notas del estudiante: " + e.getMessage());
    } finally {
        try {
            if (rs != null) rs.close();
            if (cs != null) cs.close();
            if (conn != null) conn.close();
        } catch (SQLException ex) {
        }
    }
}



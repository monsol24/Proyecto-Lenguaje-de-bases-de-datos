    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed

        int idUsuario = Integer.parseInt(txt_idusuario.getText().trim());
        String nombre = txt_nombre.getText().trim();
        String apellido = txt_apellido.getText().trim();
        String correo = txt_correo.getText().trim();
        String telefono = txt_telefono.getText().trim();

        ConexionSQL conexion = new ConexionSQL(); 
        Connection conn = conexion.conectar();

        try {
            CallableStatement cs = conn.prepareCall("{call SP_Modificar_Estudiante(?, ?, ?, ?, ?, ?)}");

            cs.setInt(1, idUsuario);
            cs.setString(2, nombre);
            cs.setString(3, apellido);
            cs.setString(4, correo);
            cs.setString(5, telefono);
            cs.registerOutParameter(6, java.sql.Types.VARCHAR);

            cs.execute();

            String mensaje = cs.getString(6);
            JOptionPane.showMessageDialog(null, mensaje);

            if (mensaje.equalsIgnoreCase("El estudiante se modificó correctamente.")) {
                txt_id.setText("");
                txt_idusuario.setText("");
                txt_nombre.setText("");
                txt_apellido.setText("");
                txt_correo.setText("");
                txt_telefono.setText("");
            }

            cs.close();
            conexion.desconectar();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error SQL: " + ex.getMessage());
        }
        
    }//GEN-LAST:event_btn_modificarActionPerformed

